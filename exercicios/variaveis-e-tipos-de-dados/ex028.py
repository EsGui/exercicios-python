'''
Faça a leitura de 3 valores e apresente como resultado a soma dos quadrados dos
três valores lidos.
'''

print('Somando 3 valores')
print('-'*30)

n1 = int(input('Digite o primeiro valor: '))
n2 = int(input('Digite o segundo valor: '))
n3 = int(input('Digite o terceiro valor: '))
print('\n')

soma = n1 + n2 + n3

print(f'{n1} + {n2} + {n3} = {soma}')
