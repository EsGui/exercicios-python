'''
Faça um programa para converter uma letra maiúscula em letra minúscula. Use a tabela
ASCII para desenvolver o problema.
'''

print('Transformando maiúsculo em minúsculo')
print('-'*30)
print('\n')

l = str(input('Digite uma letra: ')).lower()
print('\n')

print('Conversão da letra informada para minúscula')
print(f'letra: {l}')

