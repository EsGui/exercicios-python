'''
Leia uma velocidade em m/s (metros por segundo) e apresente-a convertida em km/h
(quilômetros por hora). A fórmula de conversão é: K = M * 3.6, sendo K a velocidade
em km/h e M em m/s.
'''

print('Conversão de m/s para km/h')
print('-'*30)

m = float(input('Digite uma velocidade em m/s: '))
print('\n')

k = m * 3.6

print(f'A conversão de {m}m/s para km/h fica {k}km/h')




